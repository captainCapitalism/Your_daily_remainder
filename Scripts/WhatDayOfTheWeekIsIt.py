def what_day_of_the_week_is_it(day_number):
    if day_number == 0:
        return ["Monday", "Poniedziałek"]
    if day_number == 1:
        return ["Tuesday", "Wtorek"]
    if day_number == 2:
        return ["Wednesday", "Środa"]
    if day_number == 3:
        return ["Thursday", "Czwartek"]
    if day_number == 4:
        return ["Friday", "Piątek"]
    if day_number == 5:
        return ["Saturday", "Sobota"]
    if day_number == 6:
        return ["Sunday", "Niedziela"]
